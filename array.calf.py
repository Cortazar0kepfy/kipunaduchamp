def calcular_promedio(calificaciones):
    suma = sum(calificaciones)
    return suma / len(calificaciones)
# . ..  . .  . . . . 
# Arreglo de calificaciones(también considerarlo como matriz).
calificaciones = [85, 90, 78, 92, 88]

#Calcular el promedio de calificaciones en una materia cualquiera.
promedio = calcular_promedio(calificaciones)

#lectura del promedio de calificaciones obtenidas en una materia "x";

print(f"El promedio de las calificaciones es: {promedio}")

